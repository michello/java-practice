public class Variable {
    public static void main(String[] args) {
        // variable naming convention in Java is camel case
        
        // int is 32 bit
        int myNumber;
        myNumber = 88;
        System.out.println(myNumber);

        // short is 16 bit
        short myShort = 847;
        System.out.println(myShort);

        // long is 64 bit
        long myLong = 9797;

        // double is for floating point numbers or extra precision
        double myDouble = 1.2323;

        // without the f at the end, the number is a double
        float myFloat = 1.2434f;

        // unicode, 64 bit
        char myChar = 'michelle';
        boolean myBoolean = true;

        // byte, 8 bit number
        byte myByte = 127;
    }
}